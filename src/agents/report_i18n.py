def normalize_report_language(language: str | None) -> str:
    value = str(language or "en").lower()
    return value if value in SUPPORTED_REPORT_LANGUAGES else "en"


def rt(language: str | None, key: str) -> str:
    lang = normalize_report_language(language)
    return TEXT[lang].get(key) or TEXT["en"].get(key) or key
